/*


o arquivo package.json foi criado atraves do comando:
npm install -y 
esse comando cria um projeto Node.js padrão

===========================================================================================================================================================

os arquivos package-lockjson e node_modules foram criados atraves do comando:

npm i --save-dev @babel/preset-env @babel/cli @babel/core babel-loader webpack webpack-cli regenerator-runtime core-js@2

significado de cada item digitado: 

@babel/preset-env:
Isso é como um tradutor que pega o JavaScript moderno e o converte em uma versão que funciona em navegadores mais antigos.

@babel/cli:
Ferramenta que permite você rodar o Babel diretamente no terminal, para converter seu código.

@babel/core:
O coração do Babel. É o que realmente faz a mágica de transformar seu código.

babel-loader:
Um ajudante para o Webpack entender e usar o Babel durante o processo de construção do seu projeto.

webpack:
Um organizador de arquivos. Ele pega todos os seus arquivos JavaScript, CSS, imagens, etc., e empacota tudo bonitinho em um ou mais arquivos para o navegador carregar.

webpack-cli:
A ferramenta que permite você dar comandos ao Webpack no terminal.

regenerator-runtime:
Um ajudante para quando você usa funções assíncronas no seu código (as que usam async/await), para garantir que isso funcione em todos os lugares.

core-js@2:
Uma coleção de ferramentas que adiciona funcionalidades modernas do JavaScript a navegadores antigos. A versão @2 significa que você está pegando uma versão específica dessas ferramentas.


Então, rodar esse comando é como preparar seu ambiente de desenvolvimento com todas as ferramentas que você precisa para escrever JavaScript moderno e garantir que ele vai funcionar em qualquer lugar.

=========================================================================================================================================================================

package-lock.json: Um arquivo que guarda as versões exatas dos pacotes instalados para garantir que tudo funcione igual em qualquer lugar.

node_modules: Uma pasta que guarda todas as ferramentas e bibliotecas que você instalou para que seu projeto possa usá-las.

 */


